
# Data Types :-
# 1. Numerical : 1. int 
#                2. float 
#                3. complex

# 1. int 
# (.... -10, -9, -8, ..., 5, 6, 7, 8, ...., 123, 1234, 12345,....)
data1 = (10)
print(data1)
print(type(data1))

# 2. float
# (...,10.123, -10.123, -10.122, ..., -2.224, -2.223, -2.222, ...,-1.1, 0.0, 1.234, 1.235, 1.236)
# (1.0, 1.1, 1.2, ...,1.12, 1.13)
data2 = 0.00009
data3 = 0.00022
print(data2)
print(data3)
print(type(data2))
print(type(data3))

# 3. complex
data4 = (10+20j)
print(data4)
print(type(data4))

data5 = (16+26j)
print(data5)
print(type(data5))